// 2.1. Dynamic Starry / Cosmic Background Generator
function initStarryBackground() {
  const container = document.getElementById("stars-container");
  if (!container) return;
  
  // Clear any existing stars
  container.innerHTML = "";
  
  const starCount = 80;
  for (let i = 0; i < starCount; i++) {
    const star = document.createElement("div");
    star.className = "star";
    
    // Random placement
    const x = Math.random() * 100;
    const y = Math.random() * 100;
    const size = Math.random() * 2 + 1;
    const delay = Math.random() * 4;
    const duration = Math.random() * 4 + 2;
    
    star.style.left = `${x}%`;
    star.style.top = `${y}%`;
    star.style.width = `${size}px`;
    star.style.height = `${size}px`;
    star.style.animationDelay = `${delay}s`;
    star.style.animationDuration = `${duration}s`;
    
    container.appendChild(star);
  }
}

// 2.2. Navigation Control
function setupAppNavigation() {
  const views = ["lobby", "library", "marble", "sorter", "memory"];
  
  // Set active view utility
  window.switchView = function(viewId) {
    views.forEach(v => {
      const el = document.getElementById(`view-${v}`);
      if (el) {
        el.classList.remove("active");
      }
    });
    
    const targetEl = document.getElementById(`view-${viewId}`);
    if (targetEl) {
      targetEl.classList.add("active");
    }
    
    // Special setup on entering specific view
    if (viewId === "library") {
      // Auto-click first continent tab (asia)
      const firstTab = document.getElementById("tab-asia") || document.querySelector(".season-tab");
      if (firstTab) firstTab.click();
    }
    
    // Initialize mini-games if entering their views
    if (viewId === "sorter" && typeof SorterGameModule !== "undefined") {
      SorterGameModule.initGame();
    }
    if (viewId === "memory" && typeof MemoryGameModule !== "undefined") {
      MemoryGameModule.initGame();
    }
    
    // Mascot commentary updates based on view
    updateMascotSpeech(viewId);
  };

  // Bind click handlers to cards
  bindClick("card-goto-library", () => switchView("library"));
  bindClick("card-goto-marble", () => {
    switchView("marble");
    if (typeof MarbleGameModule !== 'undefined') {
      MarbleGameModule.resetToSetup();
    }
  });
  bindClick("card-goto-sorter", () => switchView("sorter"));
  bindClick("card-goto-memory", () => switchView("memory"));

  // Bind back buttons
  bindClick("btn-library-back", () => switchView("lobby"));
  bindClick("btn-sorter-back", () => {
    if (typeof SorterGameModule !== "undefined") SorterGameModule.endGameCleanly();
    switchView("lobby");
  });
  bindClick("btn-memory-back", () => {
    if (typeof MemoryGameModule !== "undefined") MemoryGameModule.endGameCleanly();
    switchView("lobby");
  });
  
  bindClick("btn-marble-back", () => {
    if (confirm("정말 메인 화면으로 돌아가시겠습니까? 진행 중인 게임이 초기화됩니다.")) {
      switchView("lobby");
    }
  });
  bindClick("btn-home-logo", () => {
    if (typeof SorterGameModule !== "undefined") SorterGameModule.endGameCleanly();
    if (typeof MemoryGameModule !== "undefined") MemoryGameModule.endGameCleanly();
    switchView("lobby");
  });

  // Badge list modal trigger
  bindClick("btn-view-badges", () => {
    showBadgesModal();
  });
}

function bindClick(id, callback) {
  const el = document.getElementById(id);
  if (el) {
    el.addEventListener("click", callback);
  }
}

