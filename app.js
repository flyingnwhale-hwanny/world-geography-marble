// Unlock badge logic
window.unlockBadge = function(badgeId) {
  let earned = JSON.parse(localStorage.getItem("world_geo_badges") || "[]");
  if (!earned.includes(badgeId)) {
    earned.push(badgeId);
    localStorage.setItem("world_geo_badges", JSON.stringify(earned));
    updateGlobalBadgeStats();
    
    // Popup toast notification
    showBadgeToast(badgeId);
  }
};

function showBadgeToast(badgeId) {
  const badge = GAME_BADGES.find(b => b.id === badgeId);
  if (!badge) return;
  
  const toast = document.createElement("div");
  toast.className = "badge-toast glass-panel";
  toast.innerHTML = `
    <div class="toast-icon">${badge.icon}</div>
    <div class="toast-info">
      <h4>뱃지 획득! [${badge.name}]</h4>
      <p>${badge.desc}</p>
    </div>
  `;
  
  document.body.appendChild(toast);
  
  // Animation triggers
  setTimeout(() => toast.classList.add("show"), 100);
  
  // Auto remove
  setTimeout(() => {
    toast.classList.remove("show");
    setTimeout(() => toast.remove(), 500);
  }, 4000);
}

